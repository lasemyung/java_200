package section2;

public class ex2_2 {
	//2개의 변수 x와 y의 합과 평균을 구하자 x,y는 적절히 사용
		public static void main(String[] args) {
			int x;
			int y;
			
			x=32;
			y=13;
			
			System.out.println("x+y =" + ( x+y));
			System.out.println("x-y =" + ( x-y));
			System.out.println("합계는"+(x+y)+"입니다");
			System.out.println("평균은"+(x+y)/2+"입니다");
		}
}
